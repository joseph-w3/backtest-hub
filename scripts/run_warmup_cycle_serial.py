def _active_run_count(base_url: str) -> int:
    payload = _worker_runs(base_url)
    runs = payload.get("runs")
    if not isinstance(runs, list):
        raise ValueError("worker /v1/runs payload missing runs list")
    active = [run for run in runs if isinstance(run, dict) and run.get("status") in {"queued", "submitted", "running"}]
    return len(active)


def _reset_worker_cache(
    *,
    ssh_target: str,
    cache_dir: str,
    drop_page_cache: bool,
) -> dict[str, Any]:
    lines = [
        "set -euo pipefail",
        f'if [ -d "{cache_dir}" ]; then find "{cache_dir}" -mindepth 1 -maxdepth 1 -exec rm -rf -- {{}} +; fi',
        "sync",
    ]
    if drop_page_cache:
        lines.append('echo 3 > /proc/sys/vm/drop_caches')
    command = " && ".join(lines)
    completed = subprocess.run(
        ["ssh", ssh_target, command],
        check=True,
        capture_output=True,
        text=True,
    )
    return {
        "ssh_target": ssh_target,
        "cache_dir": cache_dir,
        "drop_page_cache": drop_page_cache,
        "stdout": completed.stdout,
        "stderr": completed.stderr,
        "return_code": completed.returncode,
    }


def _sample_worker_cache_stats(
    *,
    ssh_target: str,
    cache_dir: str,
) -> dict[str, Any]:
    sampled_at = _utc_timestamp_id()
    started_at = time.perf_counter()
    quoted_cache_dir = shlex.quote(cache_dir)
    command = " && ".join(
        [
            "set -euo pipefail",
            (
                f"if [ -d {quoted_cache_dir} ]; then "
                f"exists=1; bytes=$(du -sb {quoted_cache_dir} | cut -f1); "
                "else exists=0; bytes=0; fi"
            ),
            "printf '%s\\t%s\\n' \"$exists\" \"$bytes\"",
        ]
    )
    try:
        completed = subprocess.run(
            ["ssh", ssh_target, command],
            check=True,
            capture_output=True,
            text=True,
        )
        line = completed.stdout.strip().splitlines()[-1]
        exists_raw, bytes_raw = line.split("\t", 1)
        return {
            "ssh_target": ssh_target,
            "cache_dir": cache_dir,
            "sampled_at": sampled_at,
            "duration_ms": round((time.perf_counter() - started_at) * 1000, 2),
            "exists": bool(int(exists_raw)),
            "bytes": int(bytes_raw),
            "error": None,
        }
    except Exception as exc:
        return {
            "ssh_target": ssh_target,
            "cache_dir": cache_dir,
            "sampled_at": sampled_at,
            "duration_ms": round((time.perf_counter() - started_at) * 1000, 2),
            "exists": None,
            "bytes": None,
            "error": repr(exc),
        }


def _mode_order(raw: str) -> list[str]:
    known = {mode.name for mode in MODES}
    parsed = [item.strip() for item in raw.split(",") if item.strip()]
    if not parsed:
        raise ValueError("at least one mode is required")
    unknown = [item for item in parsed if item not in known]
    if unknown:
        raise ValueError(f"unknown modes: {unknown}")
    return parsed



def _utc_timestamp_id() -> str:
    return time.strftime("%Y%m%dT%H%M%SZ", time.gmtime())


def _parse_iso8601_loose(value: str) -> datetime:
    normalized = value.strip()
    if normalized.endswith("Z"):
        normalized = normalized[:-1] + "+00:00"
    if "." not in normalized:
        return datetime.fromisoformat(normalized)
    main, suffix = normalized.split(".", 1)
    if "+" in suffix:
        fraction, offset = suffix.split("+", 1)
        offset = "+" + offset
    elif "-" in suffix[1:]:
        fraction, offset = suffix.rsplit("-", 1)
        offset = "-" + offset
    else:
        fraction, offset = suffix, ""
    trimmed = fraction[:6].ljust(6, "0")
    return datetime.fromisoformat(f"{main}.{trimmed}{offset}")



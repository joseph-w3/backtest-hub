def _load_submission_assets(harness_dir: Path, mode: str, backtest_id: str) -> tuple[dict[str, Any], str, bytes, bytes, bytes]:
    mode_dir = harness_dir / mode
    bundle_path = harness_dir / "bundle" / "strategies-harness.zip"
    run_spec_path = mode_dir / "run_spec.json"
    if not mode_dir.is_dir():
        raise FileNotFoundError(f"mode directory missing: {mode_dir}")
    if not run_spec_path.is_file():
        raise FileNotFoundError(f"run_spec.json missing: {run_spec_path}")
    if not bundle_path.is_file():
        raise FileNotFoundError(f"strategy bundle missing: {bundle_path}")

    run_spec = json.loads(run_spec_path.read_text(encoding="utf-8"))
    if not isinstance(run_spec, dict):
        raise ValueError(f"run_spec must be a JSON object: {run_spec_path}")
    bundle_name, bundle_bytes = load_strategy_bundle(bundle_path)
    run_spec["backtest_id"] = backtest_id
    run_spec["strategy_bundle"] = bundle_name
    app_module = _app_module()
    runner_bytes = app_module.RUNNER_PATH.read_bytes()
    runner_support_bundle_bytes = app_module.build_runner_support_bundle_bytes()
    run_spec_bytes = (json.dumps(run_spec, ensure_ascii=True, indent=2) + "\n").encode("utf-8")
    return run_spec, bundle_name, bundle_bytes, runner_bytes, runner_support_bundle_bytes


def _percentile(values: list[float], fraction: float) -> float | None:
    if not values:
        return None
    ordered = sorted(values)
    if len(ordered) == 1:
        return ordered[0]
    position = fraction * (len(ordered) - 1)
    lower = int(position)
    upper = min(lower + 1, len(ordered) - 1)
    weight = position - lower
    return ordered[lower] * (1.0 - weight) + ordered[upper] * weight


def _progress_point(row: dict[str, Any] | None) -> dict[str, Any] | None:
    if not isinstance(row, dict):
        return None
    progress = row.get("progress")
    if not isinstance(progress, dict):
        return None
    probe = progress.get("streaming_probe")
    if not isinstance(probe, dict):
        probe = {}
    summary = progress.get("streaming_summary")
    if not isinstance(summary, dict):
        summary = {}
    cache_stats = row.get("cache_stats")
    if not isinstance(cache_stats, dict):
        cache_stats = {}
    last_progress_at = progress.get("last_progress_at")
    simulated_time = progress.get("simulated_time")
    if not isinstance(last_progress_at, str) or not isinstance(simulated_time, str):
        return None
    try:
        wall_time = _parse_iso8601_loose(last_progress_at)
        sim_time = _parse_iso8601_loose(simulated_time)
    except Exception:
        return None
    chunk_index = probe.get("chunk_index")
    return {
        "wall_time": wall_time,
        "sim_time": sim_time,
        "chunk_index": int(chunk_index) if isinstance(chunk_index, int) else None,
        "events_seen": int(summary["events_seen"]) if isinstance(summary.get("events_seen"), int) else None,
        "file_touch_files_touched": (
            int(summary["file_touch_files_touched"])
            if isinstance(summary.get("file_touch_files_touched"), int)
            else None
        ),
        "file_touch_bytes_touched": (
            int(summary["file_touch_bytes_touched"])
            if isinstance(summary.get("file_touch_bytes_touched"), int)
            else None
        ),
        "file_touch_files_total": (
            int(summary["file_touch_files_total"])
            if isinstance(summary.get("file_touch_files_total"), int)
            else None
        ),
        "file_touch_bytes_total": (
            int(summary["file_touch_bytes_total"])
            if isinstance(summary.get("file_touch_bytes_total"), int)
            else None
        ),
        "cache_dir_bytes": (
            int(cache_stats["bytes"])
            if isinstance(cache_stats.get("bytes"), int)
            else None
        ),
    }


def _progress_delta_samples(progress_rows: list[dict[str, Any]], *, skip_initial_chunks: int) -> list[dict[str, float]]:
    samples: list[dict[str, float]] = []
    previous_point: dict[str, Any] | None = None
    for row in progress_rows:
        point = _progress_point(row)
        if point is None:
            continue
        if previous_point is not None:
            sample = _progress_delta_sample(previous_point, point, skip_initial_chunks=skip_initial_chunks)
            if sample is not None:
                samples.append(sample)
        previous_point = point
    return samples


def _progress_delta_sample(
    previous_point: dict[str, Any],
    point: dict[str, Any],
    *,
    skip_initial_chunks: int,
) -> dict[str, float] | None:
    chunk_index = point.get("chunk_index")
    if not isinstance(chunk_index, int) or chunk_index <= skip_initial_chunks:
        return None
    wall_delta = (point["wall_time"] - previous_point["wall_time"]).total_seconds()
    sim_delta = (point["sim_time"] - previous_point["sim_time"]).total_seconds()
    prev_events = previous_point.get("events_seen")
    curr_events = point.get("events_seen")
    if (
        wall_delta <= 0
        or sim_delta < 0
        or not isinstance(prev_events, int)
        or not isinstance(curr_events, int)
        or curr_events < prev_events
    ):
        return None
    chunk_delta = None
    prev_chunk_index = previous_point.get("chunk_index")
    if isinstance(prev_chunk_index, int):
        chunk_delta = float(chunk_index - prev_chunk_index)
    return {
        "events_per_second": float(curr_events - prev_events) / wall_delta,
        "simulated_seconds_per_wall_second": sim_delta / wall_delta,
        "chunks_per_second": chunk_delta / wall_delta if chunk_delta is not None else 0.0,
        "new_files_touched": (
            point["file_touch_files_touched"] - previous_point["file_touch_files_touched"]
            if isinstance(previous_point.get("file_touch_files_touched"), int)
            and isinstance(point.get("file_touch_files_touched"), int)
            and point["file_touch_files_touched"] >= previous_point["file_touch_files_touched"]
            else None
        ),
        "new_bytes_touched": (
            point["file_touch_bytes_touched"] - previous_point["file_touch_bytes_touched"]
            if isinstance(previous_point.get("file_touch_bytes_touched"), int)
            and isinstance(point.get("file_touch_bytes_touched"), int)
            and point["file_touch_bytes_touched"] >= previous_point["file_touch_bytes_touched"]
            else None
        ),
        "cache_dir_bytes_growth": (
            point["cache_dir_bytes"] - previous_point["cache_dir_bytes"]
            if isinstance(previous_point.get("cache_dir_bytes"), int)
            and isinstance(point.get("cache_dir_bytes"), int)
            and point["cache_dir_bytes"] >= previous_point["cache_dir_bytes"]
            else None
        ),
    }


def _collect_unique_chunk_probes(progress_rows: list[dict[str, Any]]) -> list[dict[str, Any]]:
    probes: list[dict[str, Any]] = []
    seen: set[tuple[int, str]] = set()
    for row in progress_rows:
        progress = row.get("progress")
        if not isinstance(progress, dict):
            continue
        probe = progress.get("streaming_probe")
        if not isinstance(probe, dict):
            continue
        chunk_index = probe.get("chunk_index")
        updated_at = probe.get("updated_at")
        if not isinstance(chunk_index, int) or not isinstance(updated_at, str):
            continue
        key = (chunk_index, updated_at)
        if key in seen:
            continue
        seen.add(key)
        probes.append(probe)
    probes.sort(key=lambda item: (int(item["chunk_index"]), str(item["updated_at"])))
    return probes


def summarize_progress_rows(
    progress_rows: list[dict[str, Any]],
    *,
    skip_initial_chunks: int,
) -> dict[str, Any]:
    progress_points = [
        point
        for row in progress_rows
        if (point := _progress_point(row)) is not None
    ]
    last_progress_point = progress_points[-1] if progress_points else None
    probes = _collect_unique_chunk_probes(progress_rows)
    stable = [
        probe
        for probe in probes
        if int(probe.get("chunk_index", -1)) > skip_initial_chunks
    ]
    chunk_wall_ms = [
        float(probe["chunk_wall_ms"])
        for probe in stable
        if isinstance(probe.get("chunk_wall_ms"), (int, float))
    ]
    events_per_second = [
        float(probe["events_per_second"])
        for probe in stable
        if isinstance(probe.get("events_per_second"), (int, float))
    ]
    sim_per_wall = [
        float(probe["simulated_seconds_per_wall_second"])
        for probe in stable
        if isinstance(probe.get("simulated_seconds_per_wall_second"), (int, float))
    ]
    progress_deltas = _progress_delta_samples(progress_rows, skip_initial_chunks=skip_initial_chunks)
    progress_events_per_second = [sample["events_per_second"] for sample in progress_deltas]
    progress_sim_per_wall = [sample["simulated_seconds_per_wall_second"] for sample in progress_deltas]
    progress_chunks_per_second = [sample["chunks_per_second"] for sample in progress_deltas]
    progress_new_files_touched = [
        int(sample["new_files_touched"])
        for sample in progress_deltas
        if isinstance(sample.get("new_files_touched"), (int, float))
    ]
    progress_new_bytes_touched = [
        int(sample["new_bytes_touched"])
        for sample in progress_deltas
        if isinstance(sample.get("new_bytes_touched"), (int, float))
    ]
    progress_cache_dir_bytes_growth = [
        int(sample["cache_dir_bytes_growth"])
        for sample in progress_deltas
        if isinstance(sample.get("cache_dir_bytes_growth"), (int, float))
    ]
    return {
        "unique_chunk_probe_count": len(probes),
        "stable_chunk_probe_count": len(stable),
        "skip_initial_chunks": skip_initial_chunks,
        "last_chunk_index": probes[-1]["chunk_index"] if probes else None,
        "stable_chunk_wall_ms_median": statistics.median(chunk_wall_ms) if chunk_wall_ms else None,
        "stable_chunk_wall_ms_p90": _percentile(chunk_wall_ms, 0.9),
        "stable_events_per_second_median": statistics.median(events_per_second) if events_per_second else None,
        "stable_events_per_second_p90": _percentile(events_per_second, 0.9),
        "stable_simulated_seconds_per_wall_second_median": statistics.median(sim_per_wall) if sim_per_wall else None,
        "stable_simulated_seconds_per_wall_second_p90": _percentile(sim_per_wall, 0.9),
        "progress_delta_sample_count": len(progress_deltas),
        "progress_events_per_second_median": statistics.median(progress_events_per_second) if progress_events_per_second else None,
        "progress_events_per_second_p90": _percentile(progress_events_per_second, 0.9),
        "progress_simulated_seconds_per_wall_second_median": statistics.median(progress_sim_per_wall) if progress_sim_per_wall else None,
        "progress_simulated_seconds_per_wall_second_p90": _percentile(progress_sim_per_wall, 0.9),
        "progress_chunks_per_second_median": statistics.median(progress_chunks_per_second) if progress_chunks_per_second else None,
        "progress_file_touch_delta_sample_count": len(progress_new_files_touched),
        "progress_new_files_touched_total": sum(progress_new_files_touched) if progress_new_files_touched else None,
        "progress_new_files_touched_median": statistics.median(progress_new_files_touched) if progress_new_files_touched else None,
        "progress_new_files_touched_p90": _percentile(progress_new_files_touched, 0.9),
        "progress_new_bytes_touched_total": sum(progress_new_bytes_touched) if progress_new_bytes_touched else None,
        "progress_new_bytes_touched_median": statistics.median(progress_new_bytes_touched) if progress_new_bytes_touched else None,
        "progress_new_bytes_touched_p90": _percentile(progress_new_bytes_touched, 0.9),
        "progress_file_touch_active_ratio": (
            sum(1 for value in progress_new_files_touched if value > 0) / len(progress_new_files_touched)
            if progress_new_files_touched
            else None
        ),
        "progress_cache_dir_bytes_growth_sample_count": len(progress_cache_dir_bytes_growth),
        "progress_cache_dir_bytes_growth_total": (
            sum(progress_cache_dir_bytes_growth) if progress_cache_dir_bytes_growth else None
        ),
        "progress_cache_dir_bytes_growth_median": (
            statistics.median(progress_cache_dir_bytes_growth)
            if progress_cache_dir_bytes_growth
            else None
        ),
        "progress_cache_dir_bytes_growth_p90": _percentile(progress_cache_dir_bytes_growth, 0.9),
        "progress_cache_dir_growth_active_ratio": (
            sum(1 for value in progress_cache_dir_bytes_growth if value > 0)
            / len(progress_cache_dir_bytes_growth)
            if progress_cache_dir_bytes_growth
            else None
        ),
        "final_cache_dir_bytes": (
            last_progress_point.get("cache_dir_bytes") if last_progress_point is not None else None
        ),
        "final_file_touch_files_total": last_progress_point.get("file_touch_files_total") if last_progress_point is not None else None,
        "final_file_touch_bytes_total": last_progress_point.get("file_touch_bytes_total") if last_progress_point is not None else None,
        "final_file_touch_files_touched": last_progress_point.get("file_touch_files_touched") if last_progress_point is not None else None,
        "final_file_touch_bytes_touched": last_progress_point.get("file_touch_bytes_touched") if last_progress_point is not None else None,
    }


